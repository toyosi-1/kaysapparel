const fs = require('fs');
const path = require('path');

const ADMIN_PASSWORD = 'kaysadmin2025';
const API_URL = 'https://kaysapparel.com/.netlify/functions/admin-products';
const outputPath = path.join(__dirname, '..', 'src', 'lib', 'firebase-products.ts');

async function main() {
  const response = await fetch(API_URL, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ action: 'getProducts', adminPassword: ADMIN_PASSWORD }),
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch products: ${response.statusText}`);
  }

  const rawProducts = await response.json();
  const products = rawProducts.map((doc) => ({
    id: doc.id,
    name: doc.name || '',
    price: doc.price || 0,
    category: doc.category || '',
    sizes: doc.sizes || [],
    colors: doc.colors || [],
    images: doc.images || [],
    description: doc.description || '',
    inStock: doc.inStock !== undefined ? doc.inStock : true,
  }));

  const fileContent = `// Generated by scripts/sync-firebase-products.js
// Do not edit manually. Run 'npm run sync' to regenerate.

import { Product } from './types';

export const firebaseProducts: Product[] = ${JSON.stringify(products, null, 2)};
`;

  fs.writeFileSync(outputPath, fileContent, 'utf8');
  console.log(`Synced ${products.length} Firebase products to ${outputPath}`);
}

main().catch((err) => {
  console.error('Sync failed:', err.message);
  process.exit(1);
});
