require('dotenv').config({ path: '/Users/kingtoy/Documents/E-commerce/kaysapparel/.env.local' });
const fs = require('fs');
const path = require('path');
const { initializeApp, getApps, cert } = require('firebase-admin/app');
const { getFirestore } = require('firebase-admin/firestore');

const serviceAccountJson = process.env.FIREBASE_SERVICE_ACCOUNT_JSON;
if (!serviceAccountJson) {
  console.error('FIREBASE_SERVICE_ACCOUNT_JSON environment variable is not set');
  process.exit(1);
}

const serviceAccount = JSON.parse(serviceAccountJson);

if (getApps().length === 0) {
  initializeApp({
    credential: cert(serviceAccount),
    storageBucket: serviceAccount.project_id + '.firebasestorage.app',
  });
}

const db = getFirestore();
const outputPath = path.join(__dirname, '..', 'src', 'lib', 'firebase-products.ts');

async function main() {
  const snapshot = await db.collection('products').orderBy('createdAt', 'desc').get();
  const products = snapshot.docs.map((doc) => {
    const data = doc.data();
    return {
      id: doc.id,
      name: data.name || '',
      price: data.price || 0,
      category: data.category || '',
      sizes: data.sizes || [],
      colors: data.colors || [],
      images: data.images || [],
      description: data.description || '',
      inStock: data.inStock !== undefined ? data.inStock : true,
    };
  });

  const fileContent = `// Generated by scripts/sync-firebase-products.js
// Do not edit manually. Run 'npm run sync' to regenerate.

import { Product } from './types';

export const firebaseProducts: Product[] = ${JSON.stringify(products, null, 2)};
`;

  fs.writeFileSync(outputPath, fileContent, 'utf8');
  console.log(`Synced ${products.length} Firebase products to ${outputPath}`);
}

main().catch((err) => {
  console.error('Sync failed:', err);
  process.exit(1);
});
